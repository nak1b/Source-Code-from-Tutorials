#include <stdio.h>
#include <stdlib.h>
#include <string.h>

int main() {
	
	//Math operators
	// + Addition 
	// - Subtraction
	// / Divide
	// * Multiplication
	// % Modulos or remainder
	
	int a = 86;
	int b = 21;
	
	//Integer result (INT)
	printf("%d \n", a/b);
	
	float c = 86.0;
	float d = 21.0;
		
	//float resukt (FLOAT)
	printf("%f \n", c/d);
		
	return 0;
}